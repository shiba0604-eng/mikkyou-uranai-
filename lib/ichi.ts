export function getTodayIchi(birthIndex: number, todayIndex: number): IchiData {
  const offset = (todayIndex - birthIndex + 27) % 27;
  return ICHI_LIST[OFFSET_TO_ICHI[offset]];
}

export function getTodayMon(birthIndex: number, todayIndex: number): MonData {
  const offset = (todayIndex - birthIndex + 27) % 27;
  return MON_LIST[OFFSET_TO_MON[offset]];
}
